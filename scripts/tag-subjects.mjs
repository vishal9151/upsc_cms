/** @param {string} paper */
function defaultSubject(paper) {
  return paper.includes('paper1') ? 'general_medicine' : 'general_surgery'
}

/** @param {object} q @param {string} file */
function tagQuestion(q, file) {
  const paper = file.replace('.json', '')
  const text = `${q.question} ${q.explanation} ${q.options.join(' ')}`.toLowerCase()

  const matches = (word) =>
    word.length <= 3
      ? new RegExp(`\\b${word.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\b`, 'i').test(text)
      : text.includes(word)

  /** @type {Array<[string, number]>} */
  const scored = []
  for (const [key, words] of Object.entries(KEYWORDS)) {
    const score = words.reduce((sum, w) => sum + (matches(w) ? 1 : 0), 0)
    if (score > 0) scored.push([key, score])
  }

  scored.sort((a, b) => b[1] - a[1])
  if (scored.length === 0) return [defaultSubject(paper)]

  const topScore = scored[0][1]
  const keys = scored
    .filter(([, score]) => score >= Math.max(1, topScore - 1))
    .slice(0, 3)
    .map(([key]) => key)

  return keys.length > 0 ? keys : [defaultSubject(paper)]
}

for (const file of FILES) {
  const path = join(dataDir, file)
  const questions = JSON.parse(readFileSync(path, 'utf8'))
  const tagged = questions.map((q) => ({
    ...q,
    subject_keys: tagQuestion(q, file),
  }))
  writeFileSync(path, `${JSON.stringify(tagged, null, 2)}\n`)
  console.log(`${file}: tagged ${tagged.length} questions`)
}
